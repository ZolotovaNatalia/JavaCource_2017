package Objects;

public class Passenger {

    private int id;
    private int age;
    private String name = "";

    public Passenger(int id, int age, String name) {
        this.id = id;
        this.age = age;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }
}
